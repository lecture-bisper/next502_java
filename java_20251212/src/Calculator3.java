//    메소드의 실행 결과를 메소드 내부에서 처리
//    메소드의 실행 결과가 늘 동일함

//  2. 매개변수는 존재하고, 반환값은 없는 형태의 메소드
//    메소드 실행 시 필요한 데이터를 외부에서 전달받아 사용
