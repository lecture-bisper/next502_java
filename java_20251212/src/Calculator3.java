//    메소드 실행 시 필요한 데이터를 내부에서 선언하고, 사용하고, 삭제함
//    메소드의 실행 결과가 늘 동일함
//    메소드의 실행 결과를 메소드 외부에 전달하여 외부에서 활용할 수 있음

