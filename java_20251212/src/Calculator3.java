//  1번 방식으로 선언
  public void sum1() {
    int x = 10;
    int y = 20;
    int result = x + y;
    System.out.println("두 변수의 덧셈은 " + result + "입니다.");
  }

//  2번 방식으로 선언
  public void sum2(int x, int y) {
    int result = x + y;
    System.out.println("두 변수의 덧셈은 " + result + "입니다.");
  }

//  3번 방식으로 선언
  public int sum3() {
    int x = 10;
    int y = 20;
    int result = x + y;
    return result;
  }

//  4번 방식으로 선언
  public int sum4(int x, int y) {
    int result = x + y;
    return result;
  }
