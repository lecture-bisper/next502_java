public class BooleanEx {

  public static void main(String[] args) {

//    boolean 타입 : 참(true) 혹은 거짓(false)에 대한 정보를 저장하는 데이터 타입
//    주로 제어문 혹은 반복문에서 연산식이 논리적으로 참인지 거짓인지 판단하기 위해서 사용함
    
//    boolean 타입의 변수 stop 에 true 를 저장
    boolean stop = true;

//    제어문에서 변수 stop 의 값을 확인
    if (stop) {
      System.out.println("중지합니다.");
    }
    else {
      System.out.println("시작합니다.");
    }
  }
}















