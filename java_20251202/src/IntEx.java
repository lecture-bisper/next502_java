public class IntEx {

  public static void main(String[] args) {

//    int 타입 : 자바에서 정수를 저장하는 기본 타입, -21억 ~ +21억 까지 저장 가능
    int val1 = 10; // int 타입의 변수 val1 에 10진수 정수 10 저장
    int val2 = 012; // int 타입의 변수 val2 에 8진수 정수 10 저장
    int val3 = 0xA; // int 타입의 변수 val3 에 16진수 정수 0xA 저장

    System.out.println("val1 : " + val1);
    System.out.println("val2 : " + val2);
    System.out.println("val3 : " + val3);
  }
}















